package com.basics;

public class Statements {
    public static void main(String[] args) {
        int a = 10;

        //if statements
        if(a == 10){
            System.out.println("Hi there");
        }

        //while loop
        int count =1;
        while(count !=5){
            System.out.println(count);
            count++;
        }

        //for loop
        for(int newcount =1; newcount !=5; newcount++){
            System.out.println(count);
        }
    }
}